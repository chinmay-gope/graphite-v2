/**
 * Graphite is a modern graph library for Java that provides graph data
 * structures, graph algorithms, graph generation utilities, formatting,
 * benchmarking, and graph input/output facilities.
 *
 * <p>The library is designed around a clean service-oriented architecture
 * where algorithms are exposed through specialized APIs instead of direct
 * algorithm classes.</p>
 *
 * <h2>Features</h2>
 *
 * <ul>
 *     <li>Mutable and immutable graph implementations</li>
 *     <li>Directed and undirected graphs</li>
 *     <li>Weighted and unweighted edges</li>
 *     <li>Fluent graph builders</li>
 *     <li>Random and pattern graph generation</li>
 *     <li>Comprehensive graph algorithms</li>
 *     <li>Graph formatting and visualization</li>
 *     <li>Benchmarking framework</li>
 * </ul>
 *
 * <h2>Architecture</h2>
 *
 * <p>Graphite separates graph storage from algorithm execution.
 * Algorithms are grouped into service APIs such as traversal,
 * shortest path, minimum spanning tree, and connectivity,
 * providing a consistent and discoverable public API.</p>
 *
 * @author Chinmay
 * @since 2.0
 */

